#Atribuição simples
saldo = 500 
print(saldo, "\n") 

#Atribuição com adição
saldo = 500 
saldo += 200
print(saldo, "\n")

#Atribuição com divisão
saldo = 500 
saldo /= 5
print(saldo, "\n")

saldo = 500 
saldo //= 5
print(saldo, "\n")

#Atribuição com módulo
saldo = 500 
saldo %= 480
print(saldo, "\n")

#Atribuição com exponenciação
saldo = 10
saldo **= 3
print(saldo, "\n")



